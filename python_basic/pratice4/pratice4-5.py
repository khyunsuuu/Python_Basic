# 탈춞문자
print("백문이 불여일견\n백견이 불여일타") # \n: 줄바꿈

# 저는 "나도코딩" 입니다.
print("저는 '나도코딩' 입니다.")
print('저는 "나도코딩" 입니다.')
print("저는 \"나도코딩\" 입니다.") # \" , \' 는 문장 내에서 따옴표로 사용할 수 있다.
print('저는 \'나도코딩\' 입니다.')

# \\ : 문장 내에서 \ 로 사용할 수 있다. ex) 경로
print("C:\\Users\\dkrl0\\Desktop")

# \r : 커서를 맨 앞으로 이동시킨 후 덮어쓰기
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

#\t : Tab 역할을 한다 (4번의 스페이스)
print("Rea\tApple")